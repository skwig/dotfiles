-- NOPs
vim.keymap.set("n", "<leader>", "<NOP>", {})
vim.keymap.set("n", "<BS>", "<NOP>", {})
vim.keymap.set("n", "<CR>", "<NOP>", {})

vim.keymap.set("n", "<S-u>", "<C-r>", {}) -- Redo with shift
vim.keymap.set("n", "<S-k>", "10k", {}) -- Faster movement
vim.keymap.set("n", "<S-j>", "10j", {}) -- Faster movement
